#While loop

#Increment
number = 10
while number <=20:
    print(number)
    number+=1

#Decrement
count = 105
while count >=100:
    print(count)
    count-=1

#For Loop
for x in range(10):
    print(x)

for num in range (15,20):
    print(num)

for b in range(1,10,2):
    print(b)

languages = ["Python", "PHP", "Kotlin"] #list
for lang in languages:
    print(lang)